#include "libft.h"

void	ft_bzero(void *dst, size_t n)
{
	size_t			i;
	unsigned char	*temp;

	i = 0;
	temp = (unsigned char *)dst;
	while (i < n)
	{
		temp[i] = '\0';
		i++;
	}
}

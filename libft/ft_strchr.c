char	*ft_strchr(const char *s, int pos)
{
	char	*str;

	str = (char *)s;
	while (*str)
	{
		if (*str == pos)
		{
			return (str);
		}
		str++;
	}
	if (pos == '\0')
		return (str);
	return (0);
}
